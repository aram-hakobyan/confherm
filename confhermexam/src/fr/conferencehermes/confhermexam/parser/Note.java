package fr.conferencehermes.confhermexam.parser;

public class Note {
	private int id = 0;
	private String text = "";

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}

}
